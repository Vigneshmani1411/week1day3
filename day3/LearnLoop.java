package week1.day3;

public class LearnLoop {
public static void main(String[] args) {
	{
	for(int i=3; i<=7; i++) {
	
		System.out.println(i);
		}System.out.println("PostIncrement");
	}
	System.out.println("next");
	{
		
		for(int j=5;j>=1; --j) {
			System.out.println(j);
		}System.out.println("Pre Increment");
	}
	
	
	}
}
